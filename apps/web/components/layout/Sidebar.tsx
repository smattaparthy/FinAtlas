"use client";

import Link from "next/link";
import { usePathname, useRouter } from "next/navigation";
import { useState } from "react";

const navItems = [
  { href: "/", label: "Dashboard", icon: DashboardIcon },
  { href: "/incomes", label: "Income", icon: IncomeIcon },
  { href: "/expenses", label: "Expenses", icon: ExpenseIcon },
  { href: "/investments", label: "Investments", icon: InvestmentIcon },
  { href: "/loans", label: "Loans", icon: LoanIcon },
  { href: "/goals", label: "Goals", icon: GoalIcon },
  { href: "/charts", label: "Charts", icon: ChartIcon },
  { href: "/settings", label: "Settings", icon: SettingsIcon },
];

export function Sidebar({ isOpen, onClose }: { isOpen: boolean; onClose: () => void }) {
  const pathname = usePathname();
  const router = useRouter();
  const [loggingOut, setLoggingOut] = useState(false);

  async function handleLogout() {
    setLoggingOut(true);
    try {
      await fetch("/api/auth/logout", { method: "POST" });
      router.push("/login");
    } catch {
      setLoggingOut(false);
    }
  }

  return (
    <>
      {/* Mobile overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/50 z-40 lg:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`
          fixed top-0 left-0 z-50 h-full w-64 bg-zinc-950 border-r border-zinc-800
          transform transition-transform duration-200 ease-in-out
          lg:translate-x-0 lg:static lg:z-0
          ${isOpen ? "translate-x-0" : "-translate-x-full"}
        `}
      >
        <div className="flex flex-col h-full">
          {/* Logo */}
          <div className="p-6 border-b border-zinc-800">
            <h1 className="text-xl font-semibold text-zinc-50">FinAtlas</h1>
            <p className="text-xs text-zinc-500 mt-1">Financial Planning</p>
          </div>

          {/* Navigation */}
          <nav className="flex-1 p-4 space-y-1">
            {navItems.map((item) => {
              const isActive = pathname === item.href;
              const Icon = item.icon;
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  onClick={onClose}
                  className={`
                    flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium
                    transition-colors duration-150
                    ${
                      isActive
                        ? "bg-zinc-800 text-zinc-50"
                        : "text-zinc-400 hover:text-zinc-50 hover:bg-zinc-900"
                    }
                  `}
                >
                  <Icon className="w-5 h-5" />
                  {item.label}
                </Link>
              );
            })}
          </nav>

          {/* Logout button */}
          <div className="p-4 border-t border-zinc-800">
            <button
              onClick={handleLogout}
              disabled={loggingOut}
              className="
                flex items-center gap-3 w-full px-3 py-2.5 rounded-xl text-sm font-medium
                text-zinc-400 hover:text-zinc-50 hover:bg-zinc-900
                transition-colors duration-150 disabled:opacity-50
              "
            >
              <LogoutIcon className="w-5 h-5" />
              {loggingOut ? "Logging out..." : "Logout"}
            </button>
          </div>
        </div>
      </aside>
    </>
  );
}

